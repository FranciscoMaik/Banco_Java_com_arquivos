    private void btloginfunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btloginfunActionPerformed
        String Nome = txtloginfunuser.getText();
        String Senha = txtloginfunsenha.getText();
        String Valores = "";
        String arrayFun[] = new String[2];
        File ArquivoFunc = new File("\\Users\\Arlaxad\\Documents\\NetBeansProjects\\banco\\src\\Funcionario\\" + Nome + ".txt");
        boolean existe = ArquivoFunc.exists();
        try {
            FileReader fr = new FileReader(ArquivoFunc);
            BufferedReader br = new BufferedReader(fr);
            while(br.ready()){
                Valores = br.readLine();
            }
            br.close();
            fr.close();
            arrayFun = Valores.split(",");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(telaloginfun.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(telaloginfun.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(existe == true){
            if(arrayFun[0].equals(Nome)&&arrayFun[1].equals(Senha)){
                txtloginfunsenha.setText("");
                txtloginfunuser.setText("");
                new telaFuncionario().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Senha Incorreta!");
                txtloginfunsenha.setText("");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Funcionario n√£o cadastrado!");
            txtloginfunsenha.setText("");
            txtloginfunuser.setText("");
        }
        
    }//GEN-LAST:event_btloginfunActionPerformed

