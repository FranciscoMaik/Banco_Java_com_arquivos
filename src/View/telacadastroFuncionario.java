    private void btCadFunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadFunActionPerformed
        String Nome = txtNomeFun.getText();
        String Senha = txtsenhaFun.getText();
        boolean confirmar = false;
        File ArquivoFun = new File("\\Users\\Arlaxad\\Documents\\NetBeansProjects\\banco\\src\\Funcionario\\" + Nome + ".txt");
        boolean valor = ArquivoFun.exists();

        if (valor == false) {
            try {
                boolean criou = ArquivoFun.createNewFile();
                if (criou == true) {
                    FileWriter fw = new FileWriter(ArquivoFun);
                    BufferedWriter bw = new BufferedWriter(fw);
                    bw.write(Nome);
                    bw.write(",");
                    bw.write(Senha);

                    bw.close();
                    fw.close();

                    //new File("\\Users\\Arlaxad\\Documents\\NetBeansProjects\\banco\\src\\Funcionario\\Fun.txt").renameTo(new File(Nome));
                }
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
                txtNomeFun.setText("");
                txtsenhaFun.setText("");

            } catch (IOException ex) {
                Logger.getLogger(telacadastroFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "JÃ¡ existe um funcionario com esse nome!");
        }

    }//GEN-LAST:event_btCadFunActionPerformed

