    private void btcadcliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcadcliActionPerformed
        String Nome = txtusercli.getText();
        String Senha = txtsenhacli.getText();
        String contaV = "";
        int quantidade, conversao, result;
        if (conta == 1) {

            quantidade = Nome.length();
            conversao = Integer.parseInt(Senha);
            result = (conversao * quantidade);
            String numcont = Integer.toString(result);
            contaV = "1234" + numcont;
        } else if (conta == 2) {

            quantidade = Nome.length();
            conversao = Integer.parseInt(Senha);
            result = (conversao * quantidade);
            String numcont = Integer.toString(result);
            contaV = "1122" + numcont;

        } else if (conta == 3) {

            quantidade = Nome.length();
            conversao = Integer.parseInt(Senha);
            result = (conversao * quantidade);
            String numcont = Integer.toString(result);
            contaV = "1455" + numcont;

        }
        File ArquivoCli = new File("C:\\Users\\Arlaxad\\Documents\\NetBeansProjects\\banco\\src\\Cliente\\" + contaV + ".txt");

        boolean existe = ArquivoCli.exists();

        if (existe == false) {
            boolean criou;
            try {
                criou = ArquivoCli.createNewFile();
                if (criou == true) {
                    FileWriter fw = new FileWriter(ArquivoCli);
                    BufferedWriter bw = new BufferedWriter(fw);
                    bw.write(Nome);
                    bw.write(",");
                    bw.write(Senha);
                    bw.write(",");
                    if (conta == 1) {

                        quantidade = Nome.length();
                        conversao = Integer.parseInt(Senha);
                        result = (conversao * quantidade);
                        String numcont = Integer.toString(result);
                        contaV = "1234" + numcont;
                        bw.write(contaV);
                        bw.write(",");
                        String saldo = "0";
                        bw.write(saldo);
                    } else if (conta == 2) {

                        quantidade = Nome.length();
                        conversao = Integer.parseInt(Senha);
                        result = (conversao * quantidade);
                        String numcont = Integer.toString(result);
                        contaV = "1122" + numcont;
                        bw.write(contaV);
                        bw.write(",");
                        String saldo = "0";
                        bw.write(saldo);
                    } else if (conta == 3) {

                        quantidade = Nome.length();
                        conversao = Integer.parseInt(Senha);
                        result = (conversao * quantidade);
                        String numcont = Integer.toString(result);
                        contaV = "1455" + numcont;
                        bw.write(contaV);
                        bw.write(",");
                        String saldo = "0";
                        bw.write(saldo);
                    }

                    bw.close();
                    fw.close();
                }
                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso, seu numero da Conta √© " + contaV);
                txtsenhacli.setText("");
                txtusercli.setText("");
                cxcorrente.setSelected(false);
                cxpoupanca.setSelected(false);
                cxsalario.setSelected(false);

            } catch (IOException ex) {
                Logger.getLogger(telacadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "J√° possui um cadastro com essa conta!");
        }

    }//GEN-LAST:event_btcadcliActionPerformed

